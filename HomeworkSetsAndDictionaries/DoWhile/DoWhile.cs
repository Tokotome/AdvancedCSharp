using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DoWhile
{
    class DoWhile
    {
        static void Main(string[] args)
        {
            do
            {
                string input = System.Console.ReadLine();
            } while (input!="gosho");
        }
    }
}
